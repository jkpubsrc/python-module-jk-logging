

import typing
import datetime






class DefaultTimeStampFormatter:

	################################################################################################################################
	## Constants
	################################################################################################################################

	################################################################################################################################
	## Constructor
	################################################################################################################################

	################################################################################################################################
	## Properties
	################################################################################################################################

	################################################################################################################################
	## Helper Methods
	################################################################################################################################

	################################################################################################################################
	## Public Methods
	################################################################################################################################

	def __call__(self, t:float) -> typing.Any:
		return datetime.datetime.fromtimestamp(t).strftime("%Y-%m-%d %H:%M:%S")
